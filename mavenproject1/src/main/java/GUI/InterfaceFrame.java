    private void initTools() {

        fileChooser.setFileSelectionMode(JFileChooser.DIRECTORIES_ONLY);
        fileChooser.setAcceptAllFileFilterUsed(false);
        dirBuilder = fileChooser.getCurrentDirectory();
    }

    private boolean validateInputs() {

        if (!validator.validateJobPrefix(jobNumberTextField.getText())) {
            PopUpFrame err = new PopUpFrame(this, "Oops", "Invalid Job Number");
            err.setVisible(true);
            return false;
        }

        if (!validator.validateQuantityInput(QuantityTextField.getText())) {
            PopUpFrame err = new PopUpFrame(this, "Oops", "Invalid Quantity");
            err.setVisible(true);
            return false;
        }
        return true;
    }
