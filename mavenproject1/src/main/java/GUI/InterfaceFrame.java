    private void OKButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_OKButtonActionPerformed

        if (validateInputs()) {

            int day = this.jDatePicker1.getModel().getDay();
            int month = this.jDatePicker1.getModel().getMonth();
            int year = this.jDatePicker1.getModel().getYear();

            LocalDate date = LocalDate.of(year, month, day);

            File fullPath = Paths.get(dirBuilder.toString(), SerialNumberGenerator.zeroPadded(jobNumberTextField.getText(), 8) + "_" + day + month + 1 + year + ".csv").toFile();
            System.out.println("Writing to: " + fullPath);
            ListWriter writer = new ListWriter(fullPath);
            SerialNumberArrayGenerator rc = new SerialNumberArrayGenerator(Integer.parseInt(jobNumberTextField.getText()), date);

            writer.writeListToFile(rc.generateXSerialNumbers(Integer.parseInt(QuantityTextField.getText())));

            PopUpFrame successFrame = new PopUpFrame(this, "Success",
                    "Serial Numbers Successfully Generated");
            successFrame.setVisible(true);

        }


    }//GEN-LAST:event_OKButtonActionPerformed

